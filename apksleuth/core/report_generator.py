def _strings(report: AnalysisReport, language: str) -> list[str]:
    urls = [item for item in report.strings if item.type == "url"]
    secrets = [item for item in report.strings if item.type in {"possible_secret", "jwt"}]
    network = [item for item in report.strings if item.type in {"url", "ip", "email"}]

    lines = [f"## {_label(language, 'network_artifacts')}", ""]
    lines.extend(_string_table(network[:100], empty=_label(language, "no_network"), language=language))
    lines.extend([f"## {_label(language, 'possible_secrets')}", ""])
    lines.extend(_string_table(secrets[:100], empty=_label(language, "no_secrets"), language=language))
    if len(urls) > 100 or len(secrets) > 100:
        lines.extend([f"> {_label(language, 'truncated')}", ""])
    return lines


def _certificate(report: AnalysisReport, language: str) -> list[str]:
    cert = report.certificate
    lines = [f"## {_label(language, 'certificate')}", ""]
    lines.extend([
        f"{_label(language, 'sig_schemes')}: `{', '.join(cert.schemes) if cert.schemes else _label(language, 'unknown')}`",
        "",
    ])
    if cert.certificates:
        lines.extend([f"| {_label(language, 'subject')} | {_label(language, 'issuer')} | {_label(language, 'valid_until')} | SHA256 |", "| --- | --- | --- | --- |"])
        for item in cert.certificates:
            lines.append(f"| {_md(item.subject)} | {_md(item.issuer)} | `{_md(item.not_valid_after)}` | `{_md(item.sha256)}` |")
        lines.append("")
    elif cert.signature_files:
        lines.extend([f"| {_label(language, 'signature_file')} | {_label(language, 'size')} | SHA256 |", "| --- | ---: | --- |"])
        for item in cert.signature_files:
            lines.append(f"| `{_md(item.path)}` | {item.size} | `{item.hashes.sha256}` |")
        lines.append("")
    for note in cert.notes:
        lines.append(f"- {note}")
    if cert.notes:
        lines.append("")
    return lines


def _native_libraries(report: AnalysisReport, language: str) -> list[str]:
    lines = [f"## {_label(language, 'native_libraries')}", ""]
    if not report.native_libraries:
        lines.extend([_label(language, "no_native"), ""])
        return lines
    lines.extend([f"| {_label(language, 'abi')} | {_label(language, 'library')} | {_label(language, 'size')} | {_label(language, 'stripped')} | SHA256 |", "| --- | --- | ---: | --- | --- |"])
    for item in report.native_libraries:
        lines.append(f"| `{_md(item.abi)}` | `{_md(item.name)}` | {item.size} | `{item.stripped}` | `{item.hashes.sha256}` |")
    lines.append("")
    return lines


def _fingerprints(report: AnalysisReport, language: str) -> list[str]:
    lines = [f"## {_label(language, 'sdk_fingerprints')}", ""]
    if report.sdks:
        lines.extend([f"| SDK | {_label(language, 'type')} | {_label(language, 'matched_patterns')} |", "| --- | --- | --- |"])
        for sdk in report.sdks:
            lines.append(f"| {_md(sdk.name)} | {_md(sdk.type)} | `{_md(', '.join(sdk.matched_patterns))}` |")
        lines.append("")
    else:
        lines.extend([_label(language, "no_sdks"), ""])

    lines.extend([f"## {_label(language, 'packer_fingerprints')}", ""])
    if report.packers:
        lines.extend([f"| {_label(language, 'packers')} | {_label(language, 'confidence')} | {_label(language, 'matched_patterns')} |", "| --- | --- | --- |"])
        for packer in report.packers:
            lines.append(f"| {_md(packer.name)} | {_md(packer.confidence)} | `{_md(', '.join(packer.matched_patterns))}` |")
        lines.append("")
    else:
        lines.extend([_label(language, "no_packers"), ""])
    return lines


def _recommendations(findings: list[Finding], language: str) -> list[str]:
    lines = [f"## {_label(language, 'recommendations')}", ""]
    recommendations = []
    seen = set()
    for finding in findings:
        recommendation = _finding_recommendation(finding, language)
        if recommendation not in seen:
            seen.add(recommendation)
            recommendations.append(recommendation)
    if not recommendations:
        lines.extend([_label(language, "no_recommendations"), ""])
        return lines
    for recommendation in recommendations:
        lines.append(f"- {recommendation}")
    lines.append("")
    return lines


def _string_table(items: list[StringFinding], empty: str, language: str) -> list[str]:
    if not items:
        return [empty, ""]
    lines = [f"| {_label(language, 'type')} | {_label(language, 'severity')} | {_label(language, 'value')} | {_label(language, 'source')} |", "| --- | --- | --- | --- |"]
    for item in items:
        lines.append(f"| {_md(item.type)} | {_md(_severity_label(item.severity, language))} | `{_md(item.value)}` | `{_md(item.source)}` |")
    lines.append("")
    return lines


def _md(value: object) -> str:
    if value is None:
        return ""
    return escape(str(value)).replace("|", "\\|").replace("\n", " ")


def _plain(value: object) -> str:
    if value is None:
        return ""
    return str(value).replace("\r", " ").replace("\n", " ").strip()


def _html(value: object) -> str:
    return escape(_plain(value), quote=True)


def _format_size(size: int, language: str = "zh") -> str:
    value = float(size)
    for unit in ("bytes", "KB", "MB", "GB"):
        if value < 1024 or unit == "GB":
            byte_unit = _label(language, "bytes")
            return f"{value:.1f} {unit}" if unit != "bytes" else f"{int(value)} {byte_unit}"
        value /= 1024
    return f"{size} {_label(language, 'bytes')}"


def _overall_verdict(report: AnalysisReport, language: str = "zh") -> str:
    counts = report.statistics.get("findings_by_severity", {}) if report.statistics else {}
    confidence = report.statistics.get("findings_by_confidence", {}) if report.statistics else {}
    if language == "zh":
        if counts.get("high", 0) and confidence.get("high", 0):
            return "存在高置信高危静态风险项，建议人工复核后再发布或分发。"
        if counts.get("high", 0):
            return "存在高危静态风险项，需结合业务上下文人工复核。"
        if counts.get("medium", 0):
            return "建议复核；当前未触发高危规则。"
        if report.errors:
            return "分析完成但存在解析说明，信任结果前请确认覆盖范围。"
        return "内置静态规则未发现主要风险。"
    if counts.get("high", 0) and confidence.get("high", 0):
        return "High-confidence high-risk findings were found; manual review is recommended before release or distribution."
    if counts.get("high", 0):
        return "High-risk findings were found; review them with app context."
    if counts.get("medium", 0):
        return "Review recommended; no high-risk rule fired."
    if report.errors:
        return "Analysis completed with parser notes; review coverage before trusting results."
    return "No major built-in static-analysis risk was detected."


def _join_names(values: list[str], language: str = "zh") -> str:
    cleaned = [_plain(value) for value in values if _plain(value)]
    return ", ".join(cleaned) if cleaned else _label(language, "none")


def _join_finding_drivers(findings: list[Finding], limit: int = 4, language: str = "zh") -> str:
    groups = _ranked_finding_groups(findings, limit)
    if not groups:
        return _label(language, "none")
    return ", ".join(f"{finding_id}={count}" for finding_id, count, _ in groups)


def _severity_rank(severity: str) -> int:
    return {"high": 0, "medium": 1, "low": 2, "info": 3}.get(severity, 9)


def _ranked_finding_groups(findings: list[Finding], limit: int) -> list[tuple[str, int, Finding]]:
    counts = Counter(item.id for item in findings)
    first_by_id: dict[str, Finding] = {}
    for finding in findings:
        first_by_id.setdefault(finding.id, finding)
    groups = [(finding_id, count, first_by_id[finding_id]) for finding_id, count in counts.items()]
    return sorted(groups, key=lambda item: (_severity_rank(item[2].severity), -item[1], item[0]))[:limit]


def _summary_list(title: str, items: list[str], language: str = "zh") -> list[str]:
    lines = [f"## {title}", ""]
    if not items:
        lines.extend([f"- {_label(language, 'none')}", ""])
        return lines
    for item in items:
        lines.append(f"- {_plain(item)}")
    lines.append("")
    return lines


def _format_top_findings(findings: list[Finding], limit: int = 10, language: str = "zh") -> list[str]:
    if not findings:
        return []

    output: list[str] = []
    for _, count, finding in _ranked_finding_groups(findings, limit):
        output.append(
            f"[{_severity_label(finding.severity, language)} / {_confidence_label(finding.confidence, language)}] "
            f"{_finding_title(finding, language)} ({count}) - {_finding_evidence(finding, language)}"
        )
    return output


def _group_deep_links(items: list[dict[str, str]]) -> list[dict[str, object]]:
    grouped: dict[str, dict[str, object]] = {}
    fields = (
        ("scheme", "schemes"),
        ("host", "hosts"),
        ("port", "ports"),
        ("path", "paths"),
        ("pathPrefix", "path_prefixes"),
        ("pathPattern", "path_patterns"),
        ("mimeType", "mime_types"),
    )
    for item in items:
        component = item.get("component") or "<unknown>"
        group = grouped.setdefault(component, {"component": component})
        for source_key, output_key in fields:
            value = item.get(source_key)
            if not value:
                continue
            values = group.setdefault(output_key, [])
            if isinstance(values, list) and value not in values:
                values.append(value)
    return list(grouped.values())


def _format_deep_link_group(item: dict[str, object]) -> str:
    route_parts = []
    for key, label in (
        ("schemes", "scheme"),
        ("hosts", "host"),
        ("ports", "port"),
        ("paths", "path"),
        ("path_prefixes", "pathPrefix"),
        ("path_patterns", "pathPattern"),
        ("mime_types", "mimeType"),
    ):
        values = item.get(key)
        if isinstance(values, list) and values:
            route_parts.append(f"{label}={', '.join(str(value) for value in values)}")
    route = "; ".join(route_parts) if route_parts else "<any>"
    return f"{item.get('component', '<unknown>')} ({route})"


def _top_recommendations(findings: list[Finding], limit: int = 8, language: str = "zh") -> list[str]:
    recommendations: list[str] = []
    seen: set[str] = set()
    for finding in sorted(findings, key=lambda item: _severity_rank(item.severity)):
        recommendation = _finding_recommendation(finding, language)
        if recommendation not in seen:
            seen.add(recommendation)
            recommendations.append(recommendation)
        if len(recommendations) >= limit:
            break
    return recommendations


