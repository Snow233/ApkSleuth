def _html_findings(findings: list[Finding], language: str = "zh") -> str:
    if not findings:
        return f"<p class=\"muted\">{_html(_label(language, 'no_risks'))}</p>"
    rows = []
    for finding in findings:
        evidence = _finding_evidence(finding, language)
        review_hint = _finding_review_hint(finding, language)
        search_text = f"{finding.severity} {finding.confidence} {finding.id} {evidence} {review_hint}".lower()
        rows.append(
            f"<tr data-finding-row data-severity=\"{_html(finding.severity)}\" data-search=\"{_html(search_text)}\">"
            f"<td><span class=\"badge\">{_html(_severity_label(finding.severity, language))}</span></td>"
            f"<td>{_html(_confidence_label(finding.confidence, language))}</td>"
            f"<td><code>{_html(finding.id)}</code></td>"
            f"<td>{_html(evidence)}</td>"
            f"<td>{_html(review_hint)}</td>"
            "</tr>"
        )
    controls = (
        '<div class="controls">'
        '<div class="control">'
        f'<label for="finding-search">{_html(_label(language, "search_findings"))}</label>'
        f'<input id="finding-search" type="search" placeholder="{_html(_label(language, "search_placeholder"))}">'
        '</div>'
        '<div class="control">'
        f'<label for="finding-severity">{_html(_label(language, "severity_filter"))}</label>'
        '<select id="finding-severity">'
        f'<option value="">{_html(_label(language, "all"))}</option>'
        f'<option value="high">{_html(_label(language, "high"))}</option>'
        f'<option value="medium">{_html(_label(language, "medium"))}</option>'
        f'<option value="low">{_html(_label(language, "low"))}</option>'
        f'<option value="info">{_html(_label(language, "info"))}</option>'
        '</select>'
        '</div>'
        f'<div class="finding-meta">{_html(_label(language, "matching_findings"))}: <span id="finding-count">{len(findings)}/{len(findings)}</span></div>'
        '</div>'
        f'<p id="finding-empty" class="muted hidden">{_html(_label(language, "no_matching_findings"))}</p>'
    )
    table = (
        f"<table><tr><th>{_html(_label(language, 'severity'))}</th><th>{_html(_label(language, 'confidence'))}</th>"
        f"<th>{_html(_label(language, 'rule'))}</th><th>{_html(_label(language, 'evidence'))}</th>"
        f"<th>{_html(_label(language, 'review_hint'))}</th></tr>" + "".join(rows) + "</table>"
    )
    return controls + table


def _html_list(items: list[str], empty: str) -> str:
    if not items:
        return f'<p class="muted">{_html(empty)}</p>'
    return "<ul>" + "".join(f"<li><code>{_html(item)}</code></li>" for item in items) + "</ul>"


def _html_filter_script() -> str:
    return """<script>
(() => {
  const search = document.getElementById('finding-search');
  const severity = document.getElementById('finding-severity');
  const count = document.getElementById('finding-count');
  const empty = document.getElementById('finding-empty');
  const rows = Array.from(document.querySelectorAll('[data-finding-row]'));
  function applyFilters() {
    const query = (search?.value || '').trim().toLowerCase();
    const selectedSeverity = severity?.value || '';
    let visible = 0;
    for (const row of rows) {
      const matchesSeverity = !selectedSeverity || row.dataset.severity === selectedSeverity;
      const matchesQuery = !query || (row.dataset.search || '').includes(query);
      const show = matchesSeverity && matchesQuery;
      row.hidden = !show;
      if (show) visible += 1;
    }
    if (count) count.textContent = `${visible}/${rows.length}`;
    if (empty) empty.classList.toggle('hidden', visible !== 0);
  }
  search?.addEventListener('input', applyFilters);
  severity?.addEventListener('change', applyFilters);
  applyFilters();
})();
</script>"""


def _normalize_language(language: str) -> str:
    normalized = (language or "zh").lower()
    return normalized if normalized in SUPPORTED_LANGUAGES else "zh"


def _label(language: str, key: str) -> str:
    lang = _normalize_language(language)
    return LABELS[lang].get(key, LABELS["en"].get(key, key))


def _severity_label(severity: str, language: str) -> str:
    normalized = (severity or "").lower()
    if normalized in {"high", "medium", "low", "info"}:
        return _label(language, normalized)
    if normalized == "sensitive":
        return "敏感" if language == "zh" else "Sensitive"
    if normalized == "normal":
        return "普通" if language == "zh" else "Normal"
    return severity.upper() if language == "en" else severity


def _confidence_label(confidence: str, language: str) -> str:
    normalized = (confidence or "medium").lower()
    if language == "zh":
        return {"high": "高置信", "medium": "中置信", "low": "低置信"}.get(normalized, confidence)
    return {"high": "High", "medium": "Medium", "low": "Low"}.get(normalized, confidence.title())


def _finding_title(finding: Finding, language: str) -> str:
    if language == "zh":
        return FINDING_TITLES_ZH.get(finding.id, finding.title)
    return finding.title


def _finding_recommendation(finding: Finding, language: str) -> str:
    if language == "zh":
        return FINDING_RECOMMENDATIONS_ZH.get(finding.id, finding.recommendation)
    return finding.recommendation


def _finding_review_hint(finding: Finding, language: str) -> str:
    if language == "zh":
        return FINDING_REVIEW_HINTS_ZH.get(finding.id, finding.review_hint or "人工复核该发现项是否可被真实触发。")
    return finding.review_hint or "Manually review whether this finding is reachable in the target app context."


def _permission_recommendation(level: str, recommendation: str, language: str) -> str:
    if language != "zh":
        return recommendation
    normalized = (level or "").lower()
    if normalized == "high":
        return "确认该高危权限是否为核心功能必需；不必要时移除，并在隐私说明中明确用途。"
    if normalized == "sensitive":
        return "仅在功能必需时保留，优先使用更低敏感度的替代能力。"
    return "仅在功能确实需要时保留。"


